    private DefaultTableModel cargarTabla() {
        DefaultTableModel tModel=modelarTabla();
        try {
            lOrdenTrabajo = dOrdenTrabajo.traerTodos();
            lTecnicos = dTecnico.traerTodos();

            for (OrdenTrabajo ot : lOrdenTrabajo) {
                Tecnico tec=lTecnicos.stream().filter(t->t.getId_tecnico()==ot.getId_tecnico()).findFirst().orElse(null);
                if (tec != null) {
                    Object[] rows = {ot.getId_orden(), ot.getFecha(), ot.getTotal(), ot.getId_solicitud(), tec.getNombre()+" "+tec.getApellido(), ot.isEstado()};
                    tModel.addRow(rows);
                    tableOrdenes.setModel(tModel);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(verOrdenes.class.getName()).log(Level.SEVERE, null, ex);
        }
         return tModel;
    }

    public DefaultTableModel modelarTabla(){
                DefaultTableModel tModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        String[] columnas = {"N.Orden", "Fecha", "Total", "n.Solicitud", "Tecnico", "Estado"};
        tModel.setColumnIdentifiers(columnas);
        return tModel;
    }

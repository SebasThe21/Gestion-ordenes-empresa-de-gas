    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if(tableOrdenes.getRowCount()>0 && tableOrdenes.getSelectedRow()!=-1){
            int id_orden=Integer.parseInt(String.valueOf(tableOrdenes.getValueAt(tableOrdenes.getSelectedRow(),0)));
            DefaultTableModel tableModel=(DefaultTableModel) tableOrdenes.getModel();
            tableModel.removeRow(tableOrdenes.getSelectedRow());
            try {
                dOrden_Material.eliminar(id_orden);
                dOrden_Trabajos.eliminar(id_orden);
                dOrdenTrabajo.eliminar(id_orden);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Orden eliminada con exito. \n", "Orden Eliminada", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                Logger.getLogger(verOrdenes.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            javax.swing.JOptionPane.showMessageDialog(this, "No ha seleccionado ningun registro. \n", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

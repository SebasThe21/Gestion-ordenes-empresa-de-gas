
package com.arcos.gestion_ordenes.Logica;


public class Zona {
    
    private int id_zona;
    private String zona_nombre;

    public Zona() {
    }

    public Zona(int id_zona, String zona_nombre) {
        this.id_zona = id_zona;
        this.zona_nombre = zona_nombre;
    }

    public int getId_zona() {
        return id_zona;
    }

    public void setId_zona(int id_zona) {
        this.id_zona = id_zona;
    }

    public String getZona_nombre() {
        return zona_nombre;
    }

    public void setZona_nombre(String zona_nombre) {
        this.zona_nombre = zona_nombre;
    }
    
    
    
    
    
    
}
